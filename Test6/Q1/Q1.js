// var example
var a = 5;  //declaration
var a = 10; //re-declaration
a = 2;  //re-assigning
console.log(a);

// let example
let i = 3; //declaration
// let i = 5; This will return an error since i is already declared as we cannot redeclare
i = 4; //can be reassigned
console.log(i);

// const example
const c = 10; //declaration
// const c = 15; 
// c = 10 This will return an error since i is already declared as we cannot redeclare and reassign.
console.log(c);

let weekday = day1;
switch (weekday) {
    case day1:
        console.log('Sunday');
        break;
    case day2:
        console.log('Monday');
        break;
    case day3:
        console.log('Tuesday');
        break;
    case day4:
        console.log('Wednesday');
        break;
    case day5:
        console.log('Thursday');
        break;
    case day6:
        console.log('Friday');
        break;
    case day7:
        console.log('Saturday');
        break;
    default:
        console.log('Invalid day number');
        break;
}